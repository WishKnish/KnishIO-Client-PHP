namespace WishKnish\KnishIO\Client\Query;

use WishKnish\KnishIO\Client\Exception\KnishIOException;
use WishKnish\KnishIO\Client\Response\Response;
use WishKnish\KnishIO\Client\Response\ResponseCell;

/**
 * Class QueryToken
 * @package WishKnish\KnishIO\Client\Query
 */
class QueryCell extends Query {
    // Query
    protected static string $defaultQuery = 'query( $slug: String, $slugs: [ String! ], $limit: Int, $order: String ) { Cell( slug: $slug, slugs: $slugs, limit: $limit, order: $order )
	 	@fields
	 }';

    // Fields
    protected array $fields = [
        'slug',
        'name'
    ];

    /**
     * @param string $response
     *
     * @return ResponseCell
     * @throws KnishIOException
     */
    public function createResponse ( string $response ): ResponseCell {
        return new ResponseCell( $this, $response );
    }

}
