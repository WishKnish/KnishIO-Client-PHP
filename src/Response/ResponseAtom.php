namespace WishKnish\KnishIO\Client\Response;

/**
 * Response class for Atom queries
 * @package WishKnish\KnishIO\Client\Response
 */
class ResponseAtom extends Response {

  /**
   * @var string
   */
  protected string $dataKey = 'data.Atom';

  /**
   * Get atom instances from the response
   *
   * @return array|null
   */
  public function getInstances(): ?array {
    $data = $this->data();
    return $data['instances'] ?? null;
  }

  /**
   * Get pagination info from the response
   *
   * @return array|null
   */
  public function getPaginatorInfo(): ?array {
    $data = $this->data();
    return $data['paginatorInfo'] ?? null;
  }
}